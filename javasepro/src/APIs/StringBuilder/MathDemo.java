package APIs.StringBuilder;

public class MathDemo {
    /*math类是工具类，不能提供公开的构造器*/
    public static void main(String[] args) {
        System.out.println(Math.abs(-10));
        System.out.println(Math.ceil(4.000000000001)); // 5
        System.out.println(Math.floor(4.999999999999999)); //4
        System.out.println(Math.round(4.75)); //5 ,四舍五入
    }

}
