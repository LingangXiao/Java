package constant;

public class constantDemo {
    public static final String MY_NAME = "Sean";
    public static void main(String[] args) {
        //学会常量的使用
        //可以用于以后做项目时，经常出现的名称，比如集团的名字
        //网页中可能很多地方都会出现集团的名字
        //如果以后改了名字，一个一个改很麻烦，所以可以把集团名字直接弄成常量
        System.out.println(MY_NAME);
        System.out.println(MY_NAME);
        System.out.println(MY_NAME);
        System.out.println(MY_NAME);
        System.out.println(MY_NAME);
        System.out.println(MY_NAME);
        System.out.println(MY_NAME);
        System.out.println(MY_NAME);

    }
}
